package main

import "fmt"

func main() {

	var a = "initial" // var a string = "initial" same as example
	fmt.Println(a)

	var b, c int = 1, 2 // also same as var b,c = 1,2
	fmt.Println(b, c)

	var d = true
	fmt.Println(d)

	var e int
	fmt.Println(e)

	f := "apple" // short declaration use for declaring and initializing a variable,
	fmt.Println(f)
}
